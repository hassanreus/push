char	*ft_rot42(char *str)
{
	int		i;
	char	*r;

	r = str;
	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			r[i] = (str[i] - 'a' + 42) % 26 + 'a';
		if (str[i] >= 'A' && str[i] <= 'Z')
			r[i] = (str[i] - 'A' + 42) % 26 + 'A';
		i++;
	}
	return (r);
}
