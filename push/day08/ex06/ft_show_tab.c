#include "ft_stock_par.h"

void	ft_putnbr(int nb)
{
	unsigned int nbmr;

	if (nb < 0)
	{
		ft_putchar('-');
		nbmr = nb * -1;
	}
	else
		nbmr = nb;
	if (nbmr >= 10)
		ft_putnbr(nbmr / 10);
	ft_putchar(nbmr % 10 + '0');
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_print_words_tables(char **tab)
{
	int i;
	int j;

	i = 0;
	while (tab[i])
	{
		j = 0;
		while (tab[i][j])
		{
			ft_putchar(tab[i][j]);
			j++;
		}
		ft_putchar('\n');
		i++;
	}
}

void	ft_show_tab(struct s_stock_par *par)
{
	int i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		ft_putchar('\n');
		ft_putnbr(par[i].size_param);
		ft_putchar('\n');
		ft_print_words_tables(par[i].tab);
		i++;
	}
}
