#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c,1);
}

int main(int ac,char **av)
{
	int j = 0;
	if (ac == 2)
	{
			while (av[1][j] == ' ' || av[1][j] == '\n')
				j++;
			while(av[1][j] != '\0')
			{
				if(av[1][j] == ' ' || av[1][j] == '\n')
					break;
				ft_putchar(av[1][j]);
				j++;
			}
	}
	write(1 ,"\n", 1);
}
