#include <unistd.h>

void ft_putchar(char c)
{
		write(1, &c,1);
}

int main(int ac,char **av)
{
	int j = 0;
	if (ac == 2)
	{
		while (av[1][j])
		{
			if (av[1][j] >= 'a' && av[1][j] <= 'n')
				av[1][j] = av[1][j] + 13;
			else if (av[1][j] >= 'm' && av[1][j] <= 'z')
				av[1][j] = av[1][j] - 13;
			ft_putchar(av[1][j]);
			j++;
		}
	}
}
