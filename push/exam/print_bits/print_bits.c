#include <unistd.h>

void print_bits(unsigned int octet)
{
	int d = 128;
	while(d > 0)
	{
		if (d & octet)
			write(1,"1",1);
		else
			write(1,"0",1);
		d >>= 1;
	}
}

int main ()
{
	print_bits(2);
}
