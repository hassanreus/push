#include <stdio.h>
int		max(int* tab, unsigned int len)
{
	int  i;
	i = 0;
	int max = tab[0];
	while(len > i && tab[i] != '\0')
	{
		if (max < tab[i])
			max = tab[i];
		i++;
	}
	return max;
}

int main()
{
	int t[20] ={1, 2, 3, 4, 5, -2, -19, 42, 25, 26, -2, 43, -100, 100, 52, 1234, 928, -234};
	int m = max(t,18);
	printf("%d",m);
}
