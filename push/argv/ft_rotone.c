#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c,1);
}

int main(int ac, char **av)
{
	int j;

	j = 0;
	if(ac ==2)
	{
		while(av[1][j] != '\0')
		{
			if(av[1][j] == 'z' || av[1][j] == 'Z')
			   av[1][j] = av[1][j] - 25;
			else if((av[1][j] >= 'a' && av[1][j] <= 'y') || (av[1][j] >= 'A' && av[1][j] <= 'Y'))	
				av[1][j] = av[1][j] + 1;
			ft_putchar(av[1][j]);
			j++;
		}
	}
	ft_putchar('\n');
}
