#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*f;
	int		i;

	i = 0;
	while (src[i] != '\0')
		i++;
	f = (char*)malloc(sizeof(*f) * i + 1);
	i = 0;
	while (src[i])
	{
		f[i] = src[i];
		i++;
	}
	f[i] = '\0';
	return (f);
}
